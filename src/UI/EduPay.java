    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        // TODO add your handling code here:
        if(txtMonto.getText()==" " || txtMonto.getText()=="" || cbUsuarios.getSelectedIndex()==-1){
            JOptionPane.showMessageDialog(null, "Error, selecione datos validos", 
                    "Advertencia" ,JOptionPane.INFORMATION_MESSAGE);
        }else{
                int valor=0;
                try
                    {
                      valor = Integer.parseInt(txtMonto.getText());
                    }
                    catch(NumberFormatException e){
                        JOptionPane.showMessageDialog(null, "Error, no puede enviar fracciones de moneda, ingrese un entero", 
                    "Advertencia" ,JOptionPane.INFORMATION_MESSAGE);
                    }
		if(Logic.Login.listaUsuarios.get(correo).monedero.getSaldo()>=valor){
                    //Variables transacciones
                    String destinatario=cbUsuarios.getSelectedItem().toString();
                    int saldoAnteriorRemitente=Logic.Login.listaUsuarios.get(
                            correo).monedero.getSaldo();
                    int saldoAnteriorDestinatario=Logic.Login.listaUsuarios.get(
                            cbUsuarios.getSelectedItem().toString()).monedero.getSaldo();
                    //Realizar transacciones
                    Logic.Login.listaUsuarios.get(correo).monedero.setSaldo(
                            (saldoAnteriorRemitente-valor));
                    Logic.Login.listaUsuarios.get(destinatario).monedero.setSaldo(
                            (saldoAnteriorDestinatario+valor));
                    //AÃ±adir transacciones al historial
                    String mensajeEnvio=("Usted ha enviado: "+ valor +"EC a: "+ Logic.Login.listaUsuarios.get(cbUsuarios.getSelectedItem().toString()).getCorreo());
                    Logic.Login.listaUsuarios.get(correo).monedero.getTransaciones().add(mensajeEnvio);
                    Logic.Login.listaUsuarios.get(cbUsuarios.getSelectedItem().toString()).monedero.getTransaciones().add("Usted ha recibido: "+ valor+"EC de: "
                    + Logic.Login.listaUsuarios.get(correo).getCorreo());
                    JOptionPane.showMessageDialog(null, "Transaccion realizada con exito", 
                    "Advertencia" ,JOptionPane.INFORMATION_MESSAGE);
                }else{
                    JOptionPane.showMessageDialog(null, "Saldo insuficiente", 
                    "Advertencia" ,JOptionPane.INFORMATION_MESSAGE);
                }
                
                //Actualizacion lista
                DefaultListModel dlm=new DefaultListModel();
                for(int i=0;i<Logic.Login.listaUsuarios.get(correo).monedero.getTransaciones().size();i++){
                    dlm.addElement(Logic.Login.listaUsuarios.get(correo).monedero.getTransaciones().get(i));
                }
                listTransacciones.setModel(dlm);
        }
    }//GEN-LAST:event_btnEnviarActionPerformed

